let isValidSudoku2 = function (board) {
    let row = []
    let columns = []
    let boxes = []
    for (let i = 0; i < board.length; i++) {
        let item = board[i]
        for (let j = 0; j < board.length; j++) {
            let boxIndex = Math.floor(i / 3) * 3 + Math.floor(j / 3)
            let it = item[j]
            if (it !== '.') {
                row[i] = {...row[i]}
                columns[j] = {...columns[j]}
                boxes[boxIndex] = {...boxes[boxIndex]}
                row[i][it] = row[i][it] ? row[i][it] + 1 : 1
                columns[j][it] = columns[j][it] ? columns[j][it] + 1 : 1
                boxes[boxIndex][it] = boxes[boxIndex][it] ? boxes[boxIndex][it] + 1 : 1
                console.log(i,j, it,boxIndex, row[i][it], columns[j][it], boxes[boxIndex][it])

                if (row[i][it] >= 2 || columns[j][it] >= 2 || boxes[boxIndex][it] >= 2) {
                    return false
                }

            }

        }
    }

    return true
}

